package basics;

public class SwapTwoNumbers {

	public static void main(String[] args) {

		int a=10;
		int b= 20;
		int c=0;
		
		System.out.println("Numbers before swapping "+" a= "+ a +","+"b= "+b);
		c=a;
		a=b;
		b=c;
		System.out.println("Numbers after swapping "+" a= "+ a +","+"b= "+b);
	}

}
